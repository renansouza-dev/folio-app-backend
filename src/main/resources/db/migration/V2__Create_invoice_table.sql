CREATE TABLE IF NOT EXISTS invoices (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   date date,
   broker VARCHAR(50),
   total DECIMAL,
   fees DECIMAL,
   taxes DECIMAL,
   user_uuid UUID,
   CONSTRAINT pk_invoices PRIMARY KEY (id)
);

